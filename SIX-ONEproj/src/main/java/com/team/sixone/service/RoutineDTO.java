package com.team.sixone.service;

public class RoutineDTO {

	private String routineNo;
	private String routineName;
	private String id;
	private String goalCount;
	private String goalSet;
	private String exerciseName;
	private String routineDays;
	private String exerciseMotions;
	
	
	
	public String getExerciseMotions() {
		return exerciseMotions;
	}
	public void setExerciseMotions(String exerciseMotions) {
		this.exerciseMotions = exerciseMotions;
	}
	public String getRoutineDays() {
		return routineDays;
	}
	public void setRoutineDays(String routineDays) {
		this.routineDays = routineDays;
	}
	public String getRoutineNo() {
		return routineNo;
	}
	public void setRoutineNo(String routineNo) {
		this.routineNo = routineNo;
	}
	public String getRoutineName() {
		return routineName;
	}
	public void setRoutineName(String routineName) {
		this.routineName = routineName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGoalCount() {
		return goalCount;
	}
	public void setGoalCount(String goalCount) {
		this.goalCount = goalCount;
	}
	public String getGoalSet() {
		return goalSet;
	}
	public void setGoalSet(String goalSet) {
		this.goalSet = goalSet;
	}
	public String getExerciseName() {
		return exerciseName;
	}
	public void setExerciseName(String exerciseName) {
		this.exerciseName = exerciseName;
	}
	
	
	
	
	
}
