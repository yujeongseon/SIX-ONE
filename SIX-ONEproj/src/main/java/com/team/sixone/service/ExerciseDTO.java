package com.team.sixone.service;

public class ExerciseDTO {
	
	private String exerciseNo;
	private String exerciseName;
	private String exercisePartials;
	private String exerciseMotions;
	
	public String getExerciseNo() {
		return exerciseNo;
	}
	public void setExerciseNo(String exerciseNo) {
		this.exerciseNo = exerciseNo;
	}
	public String getExerciseName() {
		return exerciseName;
	}
	public void setExerciseName(String exerciseName) {
		this.exerciseName = exerciseName;
	}
	public String getExercisePartials() {
		return exercisePartials;
	}
	public void setExercisePartials(String exercisePartials) {
		this.exercisePartials = exercisePartials;
	}
	public String getExerciseMotions() {
		return exerciseMotions;
	}
	public void setExerciseMotions(String exerciseMotions) {
		this.exerciseMotions = exerciseMotions;
	}
	
	
	
	
	

}
