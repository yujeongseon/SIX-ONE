package com.team.sixone.service;

public class FoodDTO {
	
	private String foodNo;
	private String foodWhen;
	private String foodName;
	private String foodKcal;
	private String foodDate;
	private String id;
	
	public String getFoodNo() {
		return foodNo;
	}
	public void setFoodNo(String foodNo) {
		this.foodNo = foodNo;
	}
	public String getFoodWhen() {
		return foodWhen;
	}
	public void setFoodWhen(String foodWhen) {
		this.foodWhen = foodWhen;
	}
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public String getFoodKcal() {
		return foodKcal;
	}
	public void setFoodKcal(String foodKcal) {
		this.foodKcal = foodKcal;
	}
	public String getFoodDate() {
		return foodDate;
	}
	public void setFoodDate(String foodDate) {
		this.foodDate = foodDate;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

	

}
