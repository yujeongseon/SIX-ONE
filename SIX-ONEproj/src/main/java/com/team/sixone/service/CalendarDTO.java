package com.team.sixone.service;

public class CalendarDTO {

	private String id;
	private String count;
	private String start;
	private String end;
	private String setCount;
	private String title;
	private String exercisePartials;
	private String routineNo;
	private String backgroundColor;
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public String getSetCount() {
		return setCount;
	}
	public void setSetCount(String setCount) {
		this.setCount = setCount;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getExercisePartials() {
		return exercisePartials;
	}
	public void setExercisePartials(String exercisePartials) {
		this.exercisePartials = exercisePartials;
	}
	public String getRoutineNo() {
		return routineNo;
	}
	public void setRoutineNo(String routineNo) {
		this.routineNo = routineNo;
	}
	public String getBackgroundColor() {
		return backgroundColor;
	}
	public void setBackgroundColor(String backgroundColor) {
		this.backgroundColor = backgroundColor;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
