package com.team.sixone.service;

public class ProteinDTO {
	
	private String proteinNo;
	private String proteinName;
	private String proteinPrice;
	private String proteinKinds;
	private String proteinImage;
	private String proteinUrl;
	
	public String getProteinNo() {
		return proteinNo;
	}
	public void setProteinNo(String proteinNo) {
		this.proteinNo = proteinNo;
	}
	public String getProteinName() {
		return proteinName;
	}
	public void setProteinName(String proteinName) {
		this.proteinName = proteinName;
	}
	public String getProteinPrice() {
		return proteinPrice;
	}
	public void setProteinPrice(String proteinPrice) {
		this.proteinPrice = proteinPrice;
	}
	public String getProteinKinds() {
		return proteinKinds;
	}
	public void setProteinKinds(String proteinKinds) {
		this.proteinKinds = proteinKinds;
	}
	public String getProteinImage() {
		return proteinImage;
	}
	public void setProteinImage(String proteinImage) {
		this.proteinImage = proteinImage;
	}
	public String getProteinUrl() {
		return proteinUrl;
	}
	public void setProteinUrl(String proteinUrl) {
		this.proteinUrl = proteinUrl;
	}
	
	
	
	
	
	
	
	
	

}
